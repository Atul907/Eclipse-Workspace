package personalProgramming;

public class HelloWorld {
	
	public static void main (String args[]) {
		
		System.out.println("Hello World");					//print in next line
		
		System.out.print("Hello World With Java\n");		// \n print in next line
		
		System.out.print("Hello World With Java \n From Atul");  // \n print in next line
		
		System.out.print(" Hello Java");						// print in same line
		
		System.out.println("Atul"+"Mishra");					// concatenation without gap in output
		
		System.out.println(" Atul "+" Mishra ");				// concatenation with gap in output
		
		System.out.print("*\n**\n***\n****");	// pattern solve
		
	}
}
	